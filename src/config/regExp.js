
const regulars = {
  email: /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  password: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[$@$!%*?&#])[A-Za-z\d$@$!%*?&#]{8,}/,
  letters: /^[A-Za-z]+$/,
  name: /^([a-zA-Z ])[^']*$/,
  digit: /^\d+$/,
  float: /^\$?\d*[.,]?[0-9]?[0-9]?$/
};

export default regulars;
